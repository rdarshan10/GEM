"""Run GEM on STALE-format implicit-conflict data and score it with STALE's own judge rubric.

This is the Path-1 validation: our scenarios in STALE's schema, scored by STALE's judge
(verbatim rubric from icedreamc/STALE judge_prompts.py). It compares, on the SAME items and
SAME judge, two answerers:

  full      — STALE's baseline: dump the whole haystack into the model, then answer.
  gem       — ingest the haystack into GEM Memory (the cascade fires on the implicit conflict
              turn), then answer each probe from memory (stale facts flagged/withheld).

Three probing dimensions (STALE): dim1 state-resolution, dim2 premise-resistance, dim3 implicit
policy adaptation. Judge returns boolean pass per dim.

HONEST scope: Path 1 uses our scenarios (not STALE's official 400), and ingests user turns as
facts via GEM's ZERO-CONFIG path (derive_links infers edges) — so it tests the real end-to-end
pipeline, including the extraction/edge-inference bottleneck, not an idealised pinned-edge cascade.

Run:  python stale_eval/adapter.py --data stale_eval/path1.json --methods gem full
"""

from __future__ import annotations

import argparse
import json
import sys

sys.stdout.reconfigure(encoding="utf-8", errors="replace")

from gem import Memory
from gem.store import Status, EdgeType
from gem.llm import make_llm

# Verbatim from icedreamc/STALE Evaluation/judge_prompts.py (the scoring contract).
JUDGE_SYSTEM = """
You are a strict and impartial expert evaluator for an AI Assistant Benchmark focusing on Long-Term Memory and Implicit Reasoning.

You will be provided with the Ground Truth Context of a user's state.
Then, you will evaluate THREE distinct responses generated by a Target AI Model to THREE different probing questions (Dimension 1, 2, and 3).

[Ground Truth Context]:
- M_old: The user's outdated state.
- M_new: The user's updated state.
- Hidden Logic: How M_new invalidates M_old.

You must grade each response strictly based on whether it correctly deduces and respects the hidden updated state.

### Scoring Rubrics (Boolean: true for Pass, false for Fail)

* **Dimension 1 (Explicit Probing):** Score `true` ONLY IF Response 1 awares that the information in M_old is possibly NO LONGER VALID. Score `false` if it assumes M_old is still true or claims ignorance.

* **Dimension 2 (Adversarial Robustness):**
  Question 2 contains a FALSE PREMISE based on M_old. Score `true` ONLY IF Response 2 detects the trap, or acknowledges the new state in M_new. Score `false` if it blindly agrees with the false premise.

* **Dimension 3 (Implicit Task):**
  Score `true` ONLY IF the final action, plan, or recommendation in Response 3 is safely adheres to the NEW state established by M_new.
  Score `false` if the action complies with the outdated M_old constraints, OR if the response is too generic, vague.

Evaluate strictly based on the underlying logic, not surface-level word matching.

### Output Format (JSON)
{
  "dim1_eval": {"reasoning": "...", "pass": true/false},
  "dim2_eval": {"reasoning": "...", "pass": true/false},
  "dim3_eval": {"reasoning": "...", "pass": true/false}
}
"""

GEM_ANSWER_SYSTEM = (
    "You are a personal assistant answering from the user's stored memory. Use ONLY the memory "
    "facts provided. A fact tagged [OUTDATED] is no longer reliable — if it is relevant to the "
    "question, tell the user that detail has changed and must not be assumed. Be concise.")

# --- gemv: dedicated query-time verification (CUPMem-style readout -> premise-verify ->
# grounded answer), but powered by GEM's EXPLICIT current/stale split from the cascade, which
# CUPMem must recompute. Two focused calls per query instead of CUPMem's four-stage pipeline.
PREMISE_VERIFY_SYSTEM = (
    "You are the memory verifier for a personal assistant. You are given the user's CURRENT facts "
    "and facts that are now OUTDATED. For the user's query, decide whether the query relies on or "
    "presupposes anything that is now outdated, and state the current truth.\n"
    "Respond ONLY with JSON: {\"premise_outdated\": true/false, "
    "\"change\": \"<what changed, or 'none'>\", \"current_basis\": \"<current facts relevant to the answer>\"}")

GROUNDED_ANSWER_SYSTEM = (
    "You are a personal assistant. Use the verifier's findings to answer. If the query presupposes "
    "an OUTDATED fact, do NOT comply with that premise — tell the user what changed and answer from "
    "the current basis. If a needed detail is now outdated and unknown, say it must be reconfirmed. "
    "Be concise.")

FULL_ANSWER_SYSTEM = (
    "You are a helpful assistant. Review the following conversation history with the user, then "
    "answer the latest question accurately. Be concise.")


def user_facts(haystack):
    return [t["content"] for s in haystack for t in s if t["role"] == "user"]


def format_haystack(haystack):
    out = []
    for i, s in enumerate(haystack):
        out.append(f"=== Session {i + 1} ===")
        for t in s:
            out.append(f"{'User' if t['role'] == 'user' else 'Assistant'}: {t['content']}")
    return "\n".join(out)


def gem_memory(item):
    """Ingest the haystack's user turns as facts, in order. Zero-config: edges inferred."""
    mem = Memory()
    for fact in user_facts(item["haystack_session"]):
        mem.add(fact)
    return mem


def gem_context(mem, query, k=8):
    q = mem._g.embedder.embed(query)
    from gem.embed import cosine
    scored = [(n, cosine(q, n.embedding)) for n in mem._g.store.all_nodes()
              if n.embedding is not None]
    scored.sort(key=lambda t: t[1], reverse=True)
    lines = []
    for n, _ in scored[:k]:
        tag = " [OUTDATED]" if (n.status != Status.ACTIVE or n.meta.get("needs_review")) else ""
        lines.append(f"- {n.content}{tag}")
    return "\n".join(lines)


def gem_split_context(mem, query, k=8):
    """Readout, but split by the cascade's verdict: (current active facts, outdated facts)."""
    from gem.embed import cosine
    q = mem._g.embedder.embed(query)
    scored = [(n, cosine(q, n.embedding)) for n in mem._g.store.all_nodes()
              if n.embedding is not None]
    scored.sort(key=lambda t: t[1], reverse=True)
    cur, old = [], []
    for n, _ in scored[:k]:
        (old if (n.status != Status.ACTIVE or n.meta.get("needs_review")) else cur).append(n.content)
    return ("\n".join(f"- {c}" for c in cur) or "(none)",
            "\n".join(f"- {c}" for c in old) or "(none)")


def gemv_answer(mem, query, llm):
    """Dedicated query-time verification: premise-verify (using GEM's current/stale split) ->
    grounded answer. Two focused calls, vs CUPMem's four-stage pipeline."""
    cur, old = gem_split_context(mem, query)
    verdict = llm.chat_json(
        PREMISE_VERIFY_SYSTEM,
        f"CURRENT FACTS:\n{cur}\n\nOUTDATED FACTS:\n{old}\n\nQUERY: {query}")
    return llm.chat(
        GROUNDED_ANSWER_SYSTEM,
        f"VERIFIER FINDINGS: {json.dumps(verdict)}\n\nCURRENT FACTS:\n{cur}\n\n"
        f"OUTDATED FACTS:\n{old}\n\nQUERY: {query}")


# --- lazy (#3): NO eager cascade. Store facts in order; invalidate only the query's candidates
# at query time, against the later timeline. Cost ~O(queries) not O(haystack) — fixes the scale
# wall — and the check is query-specific, so often more accurate.
LAZY_INVALIDATE_SYSTEM = (
    "You are a memory validator. Given CANDIDATE facts relevant to a query and a bounded set of the "
    "user's RELATED observations, decide which candidates are STILL VALID and which were INVALIDATED "
    "by an observation — directly, or by implication using world knowledge. "
    "Respond ONLY JSON: {\"valid\": [\"...\"], \"invalidated\": [{\"fact\": \"...\", \"reason\": \"...\"}], "
    "\"current_basis\": \"<current truth relevant to the query>\"}")

# --- precond (#1): decompose each fact into PRECONDITIONS; a later statement that negates a
# precondition invalidates the fact. Makes the implicit conflict explicit and auditable.
PRECOND_EXTRACT_SYSTEM = (
    "Decompose a personal memory fact into its implicit PRECONDITIONS — conditions that must hold "
    "for it to stay true (capabilities, possessions, location, status, schedule). "
    "Respond ONLY JSON: {\"preconditions\": [\"...\", ...]}")

PRECOND_CHECK_SYSTEM = (
    "You validate memory via explicit preconditions. Given CANDIDATE facts WITH their preconditions "
    "and the user's later timeline, mark a candidate INVALIDATED if any later statement negates one "
    "of its preconditions. Respond ONLY JSON: {\"valid\": [\"...\"], "
    "\"invalidated\": [{\"fact\": \"...\", \"violated\": \"...\"}], \"current_basis\": \"...\"}")


def _retrieve(facts, emb, query, k=5):
    from gem.embed import cosine
    qv = emb.embed(query)
    ranked = sorted(((cosine(qv, emb.embed(f)), f) for f in facts), key=lambda t: t[0], reverse=True)
    return [f for _, f in ranked[:k]]


def lazy_answer(facts, emb, query, llm):
    # bounded readout: candidate facts + only their most-relevant related observations (scales to
    # huge haystacks — ~O(1) in timeline length), instead of sending the whole timeline.
    from methods import lazy_candidates_and_context
    cands, ctx = lazy_candidates_and_context(facts, emb, query)
    verdict = llm.chat_json(
        LAZY_INVALIDATE_SYSTEM,
        "CANDIDATE FACTS:\n" + "\n".join(f"- {c}" for c in cands) +
        "\n\nRELATED OBSERVATIONS:\n" + "\n".join(f"- {c}" for c in ctx) + f"\n\nQUERY: {query}")
    return llm.chat(GROUNDED_ANSWER_SYSTEM,
                    f"VALIDATOR FINDINGS: {json.dumps(verdict)}\n\nQUERY: {query}")


def build_preconditions(facts, llm):
    pre = {}
    for f in facts:
        out = llm.chat_json(PRECOND_EXTRACT_SYSTEM, f"FACT: {f}")
        pre[f] = out.get("preconditions", []) if isinstance(out, dict) else []
    return pre


def precond_answer(facts, preconds, emb, query, llm):
    cands = _retrieve(facts, emb, query)
    timeline = "\n".join(f"{i + 1}. {f}" for i, f in enumerate(facts))
    block = "\n".join(f"- {c}  [preconditions: {'; '.join(preconds.get(c, [])) or 'none'}]" for c in cands)
    verdict = llm.chat_json(PRECOND_CHECK_SYSTEM,
                            f"TIMELINE:\n{timeline}\n\nCANDIDATES:\n{block}\n\nQUERY: {query}")
    return llm.chat(GROUNDED_ANSWER_SYSTEM,
                    f"VALIDATOR FINDINGS: {json.dumps(verdict)}\n\nQUERY: {query}")


# --- trigger (#2): pre-enumerate invalidators at write ("what future event makes this false?").
# Detection is then a CHEAP embedding match of later observations against the trigger index — NO
# per-query LLM reasoning for detection (only the final grounded answer). Front-loads world
# knowledge to write-time; tests whether match-not-reason is enough.
TRIGGER_GEN_SYSTEM = (
    "For a personal memory fact, list the concrete FUTURE events or observations that would make it "
    "false or obsolete (an injury, a move, a purchase or sale, a diet change, a schedule change...). "
    "Respond ONLY JSON: {\"triggers\": [\"<an event that would invalidate the fact>\", ...]}")


def build_triggers(facts, emb, llm):
    trig = {}
    for f in facts:
        out = llm.chat_json(TRIGGER_GEN_SYSTEM, f"FACT: {f}")
        ts = out.get("triggers", []) if isinstance(out, dict) else []
        trig[f] = [(t, emb.embed(t)) for t in ts]
    return trig


def trigger_answer(facts, trig, emb, query, llm, thr=0.45):
    """Detection = cheap embedding match (no LLM); only the grounded answer uses the LLM."""
    from gem.embed import cosine
    fact_vecs = {f: emb.embed(f) for f in facts}
    valid, invalid = [], []
    for c in _retrieve(facts, emb, query):
        hit = None
        for obs in facts:
            if obs == c:
                continue
            for tphrase, tvec in trig.get(c, []):
                if cosine(fact_vecs[obs], tvec) >= thr:
                    hit = {"by": obs, "trigger": tphrase}
                    break
            if hit:
                break
        (invalid if hit else valid).append((c, hit))
    findings = {"valid": [c for c, _ in valid],
                "invalidated": [{"fact": c, **h} for c, h in invalid]}
    return llm.chat(GROUNDED_ANSWER_SYSTEM,
                    f"VALIDATOR FINDINGS: {json.dumps(findings)}\n\nQUERY: {query}")


# --- glazy (graph-guided lazy): EAGER direct-conflict detection (marks directly-contradicted
# facts stale at write) + LAZY propagation (walk DERIVED_FROM edges at query to reach a stale
# ancestor, then ONE bounded reasoning call to confirm — preserving semantic-stop). Recovers the
# eager cascade's accuracy (same detection + edges) at lazy's cost (propagate only what's queried).
PROPAGATE_CHECK_SYSTEM = (
    "You are given a QUERIED memory and the DEPENDENCY CHAIN it rests on, from the queried fact up "
    "to its root. Tags mark what changed: [INVALID] = the fact was invalidated; [changed by: X] = a "
    "later observation X updated it. Decide whether, given those changes, the QUERIED memory is now "
    "invalid. It may NOT be — only the specific aspect that changed matters (a time-zone survives a "
    "same-country move). Respond ONLY JSON: {\"invalidated\": true/false, \"reason\": \"...\"}")


def glazy_memory(item):
    mem = Memory(cascade=False)                       # eager-detect + edges, NO propagation
    for f in user_facts(item["haystack_session"]):
        mem.add(f)
    return mem


def _retrieve_nodes(g, query, k=4):
    from gem.embed import cosine
    qv = g.embedder.embed(query)
    scored = sorted(((cosine(qv, n.embedding), n) for n in g.store.all_nodes()
                     if n.embedding is not None), key=lambda t: t[0], reverse=True)
    return [n for _, n in scored[:k]]


def _contradictor(g, node):
    """The later fact that conflicted with `node` (CONTRADICTS edge points at it), or None."""
    for e in g.store.edges():
        if e.type == EdgeType.CONTRADICTS and e.dst_id == node.id:
            return g.store.get(e.src_id)
    return None


def _chain_context(g, node, maxhops=8):
    """Walk DERIVED_FROM leaf->root, collecting each node's CURRENT content plus change markers
    (status + contradictor). Carries the 'what changed' signal a single-ancestor check loses."""
    chain, cur, seen, changed = [], node, set(), False
    while cur and cur.id not in seen and len(chain) < maxhops:
        seen.add(cur.id)
        tag = ""
        if cur.status != Status.ACTIVE or cur.meta.get("needs_review"):
            tag += " [INVALID]"
            changed = True
        contra = _contradictor(g, cur)
        if contra:
            tag += f" [changed by: {contra.content}]"
            changed = True
        chain.append(f"{cur.content}{tag}")
        parents = g.store.derived_from_targets(cur.id)
        cur = parents[0] if parents else None
    return chain, changed


def glazy_answer(mem, query, llm):
    g = mem._g
    cur, changed = [], []
    for n in _retrieve_nodes(g, query, k=3):
        chain, has_change = _chain_context(g, n)     # lazy propagation: walk only the queried path
        if not has_change:
            cur.append(n.content)
            continue
        v = llm.chat_json(                           # one reasoning call over the reconstructed chain
            PROPAGATE_CHECK_SYSTEM,
            "DEPENDENCY CHAIN (queried fact first, then what it depends on up to the root):\n" +
            "\n".join(f"  {i + 1}. {c}" for i, c in enumerate(chain)) +
            f"\n\nQUERIED MEMORY: {n.content}")
        if isinstance(v, dict) and v.get("invalidated"):
            changed.append(f"{n.content} ({v.get('reason', 'upstream change')})")
        else:
            cur.append(n.content)
    return llm.chat(GROUNDED_ANSWER_SYSTEM,
                    "CURRENT FACTS:\n" + ("\n".join(f"- {c}" for c in cur) or "(none)") +
                    "\n\nCHANGED / INVALID:\n" + ("\n".join(f"- {c}" for c in changed) or "(none)") +
                    f"\n\nQUERY: {query}")


def answer(item, method, llm):
    qs = item["probing_queries"]
    facts = user_facts(item["haystack_session"])
    mem = emb = preconds = trig = None
    if method in ("gem", "gemv"):
        mem = gem_memory(item)                       # eager cascade at write
    elif method == "glazy":
        mem = glazy_memory(item)                     # eager-detect + edges, lazy propagation
    elif method in ("lazy", "precond", "trigger"):
        from gem.embed import default_embedder
        emb = default_embedder()
        if method == "precond":
            preconds = build_preconditions(facts, llm)
        elif method == "trigger":
            trig = build_triggers(facts, emb, llm)   # pre-enumerate invalidators at write
    resp = {}
    for dim in ("dim1_query", "dim2_query", "dim3_query"):
        q = qs[dim]
        if method == "gem":
            out = llm.chat(GEM_ANSWER_SYSTEM, f"MEMORY:\n{gem_context(mem, q)}\n\nQUESTION: {q}")
        elif method == "gemv":
            out = gemv_answer(mem, q, llm)
        elif method == "glazy":
            out = glazy_answer(mem, q, llm)
        elif method == "lazy":
            out = lazy_answer(facts, emb, q, llm)
        elif method == "precond":
            out = precond_answer(facts, preconds, emb, q, llm)
        elif method == "trigger":
            out = trigger_answer(facts, trig, emb, q, llm)
        else:
            hist = format_haystack(item["haystack_session"])
            out = llm.chat(FULL_ANSWER_SYSTEM, f"[Conversation History]\n{hist}\n\n[Question]\n{q}")
        resp[dim.replace("_query", "_response")] = out
    return resp


def judge(item, resp, llm):
    qs = item["probing_queries"]
    user = (
        f"[Ground Truth Context]\nM_old: {item['M_old']}\nM_new: {item['M_new']}\n"
        f"Hidden Logic: {item['explanation']}\n\n"
        f"[Dimension 1] Question: {qs['dim1_query']}\nResponse 1: {resp['dim1_response']}\n\n"
        f"[Dimension 2] Question: {qs['dim2_query']}\nResponse 2: {resp['dim2_response']}\n\n"
        f"[Dimension 3] Question: {qs['dim3_query']}\nResponse 3: {resp['dim3_response']}\n")
    out = llm.chat_json(JUDGE_SYSTEM, user)
    return {d: bool(out.get(f"{d}_eval", {}).get("pass", False)) for d in ("dim1", "dim2", "dim3")}


def run(data, methods):
    llm = make_llm()
    tallies = {m: {"dim1": 0, "dim2": 0, "dim3": 0} for m in methods}
    n = len(data)
    for item in data:
        print(f"\n[{item['uid']}] {item['M_old']}  ==>  {item['M_new']}", flush=True)
        for m in methods:
            resp = answer(item, m, llm)
            res = judge(item, resp, llm)
            for d in ("dim1", "dim2", "dim3"):
                tallies[m][d] += res[d]
            print(f"   {m:5} dim1={'P' if res['dim1'] else 'F'} "
                  f"dim2={'P' if res['dim2'] else 'F'} dim3={'P' if res['dim3'] else 'F'}", flush=True)
    print("\n" + "=" * 60)
    print(f"STALE-protocol results on {n} scenarios (our set; STALE judge rubric)")
    print("=" * 60)
    print(f"{'method':6} {'dim1(SR)':>10} {'dim2(PR)':>10} {'dim3(IPA)':>11} {'overall':>9}")
    for m in methods:
        t = tallies[m]
        tot = t["dim1"] + t["dim2"] + t["dim3"]
        print(f"{m:6} {t['dim1']}/{n:>8} {t['dim2']}/{n:>8} {t['dim3']}/{n:>9} "
              f"{tot}/{3 * n} ({tot / (3 * n):.0%})")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--data", default="stale_eval/path1.json")
    ap.add_argument("--methods", nargs="+", default=["gem", "full"])
    args = ap.parse_args()
    with open(args.data, encoding="utf-8") as f:
        data = json.load(f)
    data = data["data"] if isinstance(data, dict) else data
    run(data, args.methods)


if __name__ == "__main__":
    main()
